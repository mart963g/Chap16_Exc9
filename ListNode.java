package Exc_9;

public class ListNode
{
    public int data;
    public ListNode next;

    public ListNode()
    {
        this.data = 0;
        this.next = null;
    }

    public ListNode(int data)
    {
        this.data = data;
        this.next = null;
    }

    public ListNode(int data, ListNode next)
    {
        this.data = data;
        this.next = next;
    }
}
